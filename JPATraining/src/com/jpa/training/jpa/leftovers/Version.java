package com.jpa.training.jpa.leftovers;

public class Version {
	private int major;
	private int minor;
	private int build;

	public int getMajor() {
		return this.major;
	}

	public void setMajor(final int major) {
		this.major = major;
	}

	public int getMinor() {
		return this.minor;
	}

	public void setMinor(final int minor) {
		this.minor = minor;
	}

	public int getBuild() {
		return this.build;
	}

	public void setBuild(final int build) {
		this.build = build;
	}

}
